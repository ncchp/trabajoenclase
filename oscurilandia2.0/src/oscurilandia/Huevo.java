public class Huevo {
    private int filaC;
    private int columnaC;
    
    public Huevo(int filaC, int columnaC) {
        this.filaC = filaC;
        this.columnaC = columnaC;
    }
    public int getFilaC() {
        return filaC;
    }
    public void setFilaC(int filaC) {
        this.filaC = filaC;
    }
    public int getColumnaC() {
        return columnaC;
    }
    public void setColumnaC(int columnaC) {
        this.columnaC = columnaC;
    }
    }